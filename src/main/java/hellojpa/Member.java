package hellojpa;

import jakarta.persistence.Entity;
import jakarta.persistence.Id;

@Entity //JPA 사용 인식대상으로 명시
public class Member {

    @Id
    private Long id;
    private String name;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
